    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        if(txtNome.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Digite um Nome");
            
        }
        else if (txtPreco.getText().equals(""))
        {
         JOptionPane.showMessageDialog(this,"Digite um preço");
        }
        else
            try
            {
                Connection c = Conexao.criarConexao();
                Produtos novo = new Produtos(txtNome.getText(),
                                (Double.parseDouble(txtPreco.getText())));
                
               CRUD.alterar(novo,c);
               c.close();
               txtNome.setText("");
               txtPreco.setText("");
               popularTabela();
               
                
            }
            catch (ClassNotFoundException e) 
            {
                
            }
            catch(SQLException e)
            {
                
            }
    }//GEN-LAST:event_UpdateActionPerformed

