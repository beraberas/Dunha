    public void popularTabela()
    {
        tabTodos.removeAll(); //limpa a tela
        List<Produtos> produto = new ArrayList<Produtos>();
        Produtos rampa = null;
        
        //Pega os dados do Banco de Dados
        try
        {
            Connection c = Conexao.criarConexao();
            ResultSet dados = CRUD.mostrarTudo(c);
            while(dados.next())
            {
                rampa = new Produtos(dados.getString("nome"),
                                     dados.getDouble("preco"));
                                    
                produto.add(rampa);
            }
            
            c.close();
        }
        catch(ClassNotFoundException er)
        {
        
        }
        catch(SQLException er)
        {
            
        }
        
        //cria o modelo e associar com o list
        eModel = new lojaVirtualModeloDeTabela(produto);
        
        //Associa o modelo à tabela
        tabTodos.setModel(eModel);  //tabTodos aqui é o JTable no Visual
        
    }
